CREATE SCHEMA TAMUC_AUTOMOBILES_G3;

USE TAMUC_AUTOMOBILES_G3;

CREATE TABLE Manufacturer (
ManufacturerID INT(4) PRIMARY KEY NOT NULL,
ManufacturerName VARCHAR(50) NOT NULL,
Address VARCHAR(50) NOT NULL,
City VARCHAR(50) NOT NULL,
State VARCHAR(50) NOT NULL,
Country VARCHAR(50) NOT NULL,
ZipCode NVARCHAR(10)
);
CREATE TABLE Inventory (
InventoryID INT(3) NOT NULL PRIMARY KEY,
ManufacturerID INT(4) NOT NULL,
Make VARCHAR(50) NOT NULL,
Model VARCHAR(50) NOT NULL,
MinReqInventory INT(2) NOT NULL,
NewVehInStock INT(3) NOT NULL,
UsedVehInStock INT(3) NOT NULL,
FOREIGN KEY (ManufacturerID) REFERENCES Manufacturer(ManufacturerID)
);

CREATE TABLE VehicleDetails (
VINNo BIGINT(17) PRIMARY KEY NOT NULL,
Make VARCHAR(50) NOT NULL,
Model NVARCHAR(50) NOT NULL,
Mileage FLOAT(4) NOT NULL,
TypeofCar VARCHAR(4) NOT NULL,
YearofManufacture YEAR NOT NULL,
Location VARCHAR(10) NOT NULL,
ManufacturerID INT(4) NOT NULL,
InventoryID INT(3) NOT NULL,
FOREIGN KEY (ManufacturerID) REFERENCES Manufacturer(ManufacturerID),
FOREIGN KEY (InventoryID) REFERENCES Inventory(InventoryID)
);

CREATE TABLE ManufacturerOrders (
OrderID INT(5) NOT NULL PRIMARY KEY,
InventoryID INT(3) NOT NULL,
ManufacturerID INT(4) NOT NULL,
OrderDate DATE NOT NULL,
Model VARCHAR(50) NOT NULL,
Quantity INT(2) NOT NULL,
FOREIGN KEY (ManufacturerID) REFERENCES Manufacturer(ManufacturerID),
FOREIGN KEY (InventoryID) REFERENCES Inventory(InventoryID)
);

CREATE TABLE Employees (
EmployeeID INT(4) NOT NULL PRIMARY KEY,
FirstName VARCHAR(50) NOT NULL,
LastName VARCHAR(50) NOT NULL,
ContactNo VARCHAR(12) NOT NULL,
EmailID NVARCHAR(50) NOT NULL,
Address VARCHAR(50) NOT NULL,
City VARCHAR(50) NOT NULL,
Country VARCHAR(50),
WorkLocation VARCHAR(5)
);

CREATE TABLE SalesLeads (
LeadID INT(4) NOT NULL PRIMARY KEY,
CustomerFName VARCHAR(50) NOT NULL ,
CustomerLName VARCHAR(50) NOT NULL,
ContactNo VARCHAR(12) NOT NULL,
MailID NVARCHAR(50),
Make VARCHAR(50) NOT NULL,
Model NVARCHAR(50) NOT NULL,
TypeofCar VARCHAR(10) NOT NULL,
EmployeeID INT(4) NOT NULL,
LeadLocation VARCHAR(10) NOT NULL,
FOREIGN KEY (EmployeeID) REFERENCES Employees(EmployeeID)
);

CREATE TABLE TestDrive (
TestID INT(5) NOT NULL PRIMARY KEY,
LeadID INT(4) NOT NULL,
TestDate DATE NOT NULL,
TestStartTime time,
TestEndTime time,
EmployeeID INT(4) NOT NULL,
FOREIGN KEY (LeadID) REFERENCES SalesLeads(LeadID),
FOREIGN KEY (EmployeeID) REFERENCES Employees(EmployeeID)
);

CREATE TABLE CustomerExperienceLog (
ExperienceLogID INT(10) NOT NULL PRIMARY KEY,
TestID INT(5) NOT NULL,
Feedback VARCHAR(250),
Questions VARCHAR(200),
FOREIGN KEY (TestID) REFERENCES TestDrive(TestID)
);

CREATE TABLE Quotation (
QuotationID INT(6) NOT NULL PRIMARY KEY,
LeadID INT(4) NOT NULL,
QuotedAmount VARCHAR(20) NOT NULL,
Negotiations NVARCHAR(20),
RevisedPrice VARCHAR(20) NOT NULL,
Currency VARCHAR(20) ,
NumberOfVisits INT(3),
EmployeeID INT(4) NOT NULL,
FOREIGN KEY (EmployeeID) REFERENCES Employees(EmployeeID),
FOREIGN KEY (LeadID) REFERENCES SalesLeads(LeadID)
);

CREATE TABLE CreditCheck (
FinancialID BIGINT(16) NOT NULL PRIMARY KEY,
LeadID INT(4) NOT NULL,
CreditScore VARCHAR(10) NOT NULL,
PaymentMethod NVARCHAR(100) ,
FOREIGN KEY (LeadID) REFERENCES SalesLeads(LeadID)
);

CREATE TABLE TradeIN (
TradingID INT(6) NOT NULL PRIMARY KEY,
QuotationID INT(6) NOT NULL,
TradedMake VARCHAR(50) ,
TradedModel VARCHAR(50) ,
YearofManufacturer VARCHAR(4),
Mileage FLOAT(4) ,
ConditionofVeh VARCHAR(225),
TradedCarValue FLOAT(20) NOT NULL,
FOREIGN KEY (QuotationID) REFERENCES Quotation(QuotationID)
);

CREATE TABLE Customers (
CustomerID INT(8) NOT NULL PRIMARY KEY,
FirstName VARCHAR(50) ,
LastName VARCHAR(50) ,
Phone VARCHAR(12),
Email NVARCHAR(50)
);

CREATE TABLE CustomerOrders (
OrderID INT(6) NOT NULL PRIMARY KEY,
OrderDate DATE NOT NULL,
Make VARCHAR(50) NOT NULL,
Model VARCHAR(50) NOT NULL,
TypeofCar VARCHAR(50) NOT NULL,
QuotationID INT(6) NOT NULL,
TradingID INT(6),
Location VARCHAR(10) NOT NULL,
EmployeeID INT(4) NOT NULL,
CustomerID int(8) NOT NULL,
FOREIGN KEY (QuotationID) REFERENCES Quotation(QuotationID),
FOREIGN KEY (TradingID) REFERENCES TradeIn(TradingID),
FOREIGN KEY (EmployeeID) REFERENCES Employees(EmployeeID),
FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID)
);



CREATE TABLE Address (
AddressID INT PRIMARY KEY NOT NULL,
CustomerID INT(8) NOT NULL,
Address NVARCHAR(100) ,
City VARCHAR(50) ,
Country VARCHAR(50) NOT NULL,
LatitudeLocation NVARCHAR(50),
LongitudeLocation NVARCHAR(50),
FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID)
);

CREATE TABLE Accounting (
BillNo INT(6) NOT NULL PRIMARY KEY,
BillDate DATE NOT NULL,
Make VARCHAR(50) NOT NULL,
Model NVARCHAR(50) NOT NULL,
VINNo BIGINT(17) ,
UnitPrice FLOAT(10) ,
TaxAmt FLOAT(10) ,
TradingValue FLOAT(10),
Downpay FLOAT(10) ,
TotalAmt FLOAT(10) ,
CustomerID INT(8) NOT NULL,
EmployeeID INT(4),
BillLocation VARCHAR(10),
FOREIGN KEY (VINNo) REFERENCES Vehicledetails(VINNo),
FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID),
FOREIGN KEY (EmployeeID) REFERENCES Employees(EmployeeID)
);


